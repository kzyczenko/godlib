	XDEF	_Ssd_RepStart
	XDEF	_Ssd_RepPlay
	XDEF	_Ssd_RepStop

	XREF	_gSsdClass
	XREF	_gSsdReplay


**************************************************************************************
;	STRUCTS
**************************************************************************************


**************************************************************************************
;	EQUATES
**************************************************************************************

eSSD_VTABLE					EQU	28


**************************************************************************************
	TEXT
**************************************************************************************

*------------------------------------------------------------------------------------*
* FUNCTION : Ssd_Start( U8 * apVoiceSet, U8 * apSong )
* ACTION   : starts a song
* CREATION : 10.01.01 PNK
*------------------------------------------------------------------------------------*

_Ssd_RepStart:
	movem.l	d0-a6,-(a7)
	jsr		_gSsdReplay+eSSD_VTABLE+0
	movem.l	(a7)+,d0-a6
	rts


*------------------------------------------------------------------------------------*
* FUNCTION : Ssd_Play( void )
* ACTION   : interrupt based player
* CREATION : 10.01.01 PNK
*------------------------------------------------------------------------------------*

_Ssd_RepPlay:
	movem.l	d0-a6,-(a7)
	jsr		_gSsdReplay+eSSD_VTABLE+4
	movem.l	(a7)+,d0-a6
	rts


*------------------------------------------------------------------------------------*
* FUNCTION : Ssd_Stop( void )
* ACTION   : stops tune
* CREATION : 10.01.01 PNK
*------------------------------------------------------------------------------------*

_Ssd_RepStop:
	movem.l	d0-a6,-(a7)
	jsr		_gSsdReplay+eSSD_VTABLE+8
	movem.l	(a7)+,d0-a6
	rts


